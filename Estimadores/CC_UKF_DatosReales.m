%% ========================================================================
%        COULOMB COUNTING vs UKF + CC
%     ECM THEVENIN 1RC - DATOS EXPERIMENTALES
% ========================================================================

%% ========================================================================
% LOAD DATASET
%% ========================================================================

fileName = ...
'AUTO_SAVE_2026-05-19_120247.mat';

if exist(fileName,'file')

    load(fileName);

else

    error('Dataset file not found.');

end

%% ========================================================================
% EXTRACT DATA
%% ========================================================================

data = logSesion;

%% ========================================================================
% TIME VECTOR
%% ========================================================================

t_raw = data(:,1);

if max(t_raw) > 1e5

    t_sec = (t_raw - t_raw(1))*24*3600;

else

    t_sec = t_raw - t_raw(1);

end

%% ========================================================================
% EXPERIMENTAL SIGNALS
%% ========================================================================

V_exp = data(:,2);

I_raw = data(:,3);

N = length(I_raw);

%% ========================================================================
% CURRENT CORRECTION
%% ========================================================================

current_offset = 0.02;

% Corriente positiva durante descarga
I_corr = -(I_raw + current_offset);

% =====================================================
% DEADZONE DE CORRIENTE
% =====================================================

I_corr(abs(I_corr) < 0.08) = 0;

fprintf('\n=================================================\n');
fprintf('CURRENT INFORMATION\n');
fprintf('=================================================\n');

fprintf('Raw current mean       = %.6f A\n', mean(I_raw));
fprintf('Corrected current mean = %.6f A\n', mean(I_corr));

%% ========================================================================
% BATTERY PARAMETERS
%% ========================================================================

Cn  = 5.2;

eta = 1;

Qn = Cn * 3600;

%% ========================================================================
% SAMPLING TIME
%% ========================================================================

Ts = mean(diff(t_sec));

fprintf('Sampling time Ts = %.4f s\n', Ts);

%% ========================================================================
% SOC REAL (CONTEO DE CORRIENTE EXPERIMENTAL)
%% ========================================================================

I_real = -I_raw;

I_real(abs(I_real) < 0.08) = 0;

SOC_real = zeros(N,1);

SOC_real(1) = 0.98;

for k = 2:N

    SOC_real(k) = SOC_real(k-1) ...
                - (eta*Ts/Qn)*I_real(k);

    SOC_real(k) = max(0.01,min(0.99,SOC_real(k)));

end

%% ========================================================================
% ECM PARAMETERS - DESCARGA
%% ========================================================================

Rint = 0.50523032;

R0   = 0.55510210;

C0   = 5380.22;

%% ========================================================================
% COULOMB COUNTING
%% ========================================================================

SOC_cc = zeros(N,1);

SOC_cc(1) = SOC_real(1);

for k = 2:N

    SOC_cc(k) = SOC_cc(k-1) ...
              - (eta * Ts / Qn) * I_corr(k);

    SOC_cc(k) = max(0.01, min(0.99, SOC_cc(k)));

end

%% ========================================================================
% UKF CONFIGURATION
%% ========================================================================

L = 1;

alpha_ukf = 1e-3;

beta_ukf = 2;

kappa_ukf = 0;

lambda = alpha_ukf^2 * (L + kappa_ukf) - L;

gamma = sqrt(L + lambda);

%% ========================================================================
% UKF WEIGHTS
%% ========================================================================

Wm = zeros(2*L+1,1);

Wc = zeros(2*L+1,1);

Wm(1) = lambda/(L+lambda);

Wc(1) = Wm(1) + (1-alpha_ukf^2+beta_ukf);

for i = 2:2*L+1

    Wm(i) = 1/(2*(L+lambda));

    Wc(i) = Wm(i);

end

%% ========================================================================
% NOISE MATRICES
%% ========================================================================

Q_ukf = 1e-8;

R_ukf = 0.9^2;

%% ========================================================================
% INITIALIZATION
%% ========================================================================

x_est = zeros(L,N);

P = 1e-4;

x_est(:,1) = SOC_cc(1);

SOC_ukf = zeros(N,1);

SOC_ukf(1) = x_est(:,1);

%% ========================================================================
% ESTIMATED VOLTAGE
%% ========================================================================

V_est = zeros(N,1);

V_est(1) = V_exp(1);

%% ========================================================================
% UKF LOOP
%% ========================================================================

for k = 2:N

    %% ================================================================
    % SIGMA POINTS
    %% ================================================================

    S = sqrt(P);

    Xsigma = zeros(L,2*L+1);

    Xsigma(:,1) = x_est(:,k-1);

    for i = 1:L

        Xsigma(:,i+1) = x_est(:,k-1) + gamma*S;

        Xsigma(:,i+1+L) = x_est(:,k-1) - gamma*S;

    end

    %% ================================================================
    % PROCESS MODEL
    %% ================================================================

    Xsigma_pred = zeros(L,2*L+1);

    for i = 1:2*L+1

        SOC_i = Xsigma(1,i);

        SOC_pred = SOC_i ...
                 - (eta * Ts / Qn) * I_corr(k);

        SOC_pred = max(0.01, min(0.99, SOC_pred));

        Xsigma_pred(:,i) = SOC_pred;

    end

    %% ================================================================
    % PREDICTED MEAN
    %% ================================================================

    x_pred = 0;

    for i = 1:2*L+1

        x_pred = x_pred + Wm(i) * Xsigma_pred(:,i);

    end

    %% ================================================================
    % PREDICTED COVARIANCE
    %% ================================================================

    P_pred = 0;

    for i = 1:2*L+1

        diff = Xsigma_pred(:,i) - x_pred;

        P_pred = P_pred + Wc(i) * (diff * diff');

    end

    P_pred = P_pred + Q_ukf;

    %% ================================================================
    % MEASUREMENT MODEL
    %% ================================================================

    Ysigma = zeros(1,2*L+1);

    for i = 1:2*L+1

        SOC_i = Xsigma_pred(1,i);

        %% ============================================================
        % VOC ACTUAL
        %% ============================================================

        Voc_k = 16.4800*(SOC_i.^3) ...
              - 35.0493*(SOC_i.^2) ...
              + 27.6924*(SOC_i) ...
              + 2.8081;

        %% ============================================================
        % VOC PREVIO
        %% ============================================================

        SOC_prev = SOC_ukf(max(k-1,1));

        Voc_km1 = 16.4800*(SOC_prev.^3) ...
                - 35.0493*(SOC_prev.^2) ...
                + 27.6924*(SOC_prev) ...
                + 2.8081;

        %% ============================================================
        % THEVENIN 1RC DISCRETO
        %% ============================================================

        expTerm = exp(-Ts/(R0*C0));

        Ysigma(i) = Voc_k ...
                  + (V_exp(k-1) - Voc_km1)*expTerm ...
                  - (R0 - (R0 + Rint)*expTerm)*I_corr(k-1) ...
                  - Rint*I_corr(k);

    end

    %% ================================================================
    % PREDICTED MEASUREMENT
    %% ================================================================

    y_pred = 0;

    for i = 1:2*L+1

        y_pred = y_pred + Wm(i)*Ysigma(i);

    end

    %% ================================================================
    % INNOVATION COVARIANCE
    %% ================================================================

    Pyy = 0;

    for i = 1:2*L+1

        diff = Ysigma(i) - y_pred;

        Pyy = Pyy + Wc(i)*(diff*diff');

    end

    Pyy = Pyy + R_ukf;

    %% ================================================================
    % CROSS COVARIANCE
    %% ================================================================

    Pxy = 0;

    for i = 1:2*L+1

        dx = Xsigma_pred(:,i) - x_pred;

        dy = Ysigma(i) - y_pred;

        Pxy = Pxy + Wc(i)*(dx*dy);

    end

    %% ================================================================
    % KALMAN GAIN
    %% ================================================================

    K = Pxy / Pyy;

    %% ================================================================
    % UPDATE
    %% ================================================================

    x_update = x_pred ...
             + K * (V_exp(k) - y_pred);

    %% ================================================================
    % SOC LIMITS
    %% ================================================================

    x_update = max(0.01, min(0.99, x_update));

    %% ================================================================
    % SAVE STATES
    %% ================================================================

    x_est(:,k) = x_update;

    P = P_pred - K * Pyy * K';

    SOC_ukf(k) = x_est(:,k);

    V_est(k) = y_pred;

end

%% ========================================================================
% RMSE
%% ========================================================================

RMSE_CC = ...
sqrt(mean((SOC_cc - SOC_real).^2));

RMSE_UKF = ...
sqrt(mean((SOC_ukf - SOC_real).^2));

RMSE_voltage = ...
sqrt(mean((V_exp - V_est).^2));

fprintf('\n=================================================\n');
fprintf('ESTIMATION RESULTS\n');
fprintf('=================================================\n');

fprintf('RMSE Coulomb Counting = %.6f\n', RMSE_CC);

fprintf('RMSE UKF + CC         = %.6f\n', RMSE_UKF);

fprintf('Voltage RMSE          = %.6f V\n', RMSE_voltage);

