clear; clc; close all;

%% ========================================================================
%                   DIGITAL TWIN - BATERÍA
%                    VERSIÓN CORREGIDA
% ========================================================================

%% ========================================================================
% CONFIGURACIÓN GENERAL
%% ========================================================================

Ts = 1;                     % segundos

sim_hours = 30;             % horas simulación

t_total = sim_hours*3600;

N = floor(t_total/Ts);

%% ========================================================================
% VECTOR TIEMPO
%% ========================================================================

t = (0:N-1)*Ts;

%% ========================================================================
% BATERÍA
%% ========================================================================

Cn   = 5.2;                 % Ah
eta  = 1;

%% ========================================================================
% CONFIGURACIÓN CICLADO
%% ========================================================================

I_charge = -0.5;            %A
I_discharge = 0.5;          %A

SOC_max = 0.95;

V_min = 9.2;

rest_time = 300;            % segundos

%% ========================================================================
% PULSOS
%% ========================================================================

pulse_period = 600;         % 10 min ON
rest_period  = 120;         % 2 min OFF

%% ========================================================================
% VARIABLES
%% ========================================================================

SoC = zeros(N,1);
Vt  = zeros(N,1);
I   = zeros(N,1);
Vrc = zeros(N,1);

cycle_number = zeros(N,1);

%% ========================================================================
% ESTADO INICIAL
%% ========================================================================

SoC(1) = 0.82;

%% ========================================================================
% VOLTAJE INICIAL
%% ========================================================================

Voc0 = 16.4800*(SoC(1).^3) ...
     - 35.0493*(SoC(1).^2) ...
     + 27.6924*(SoC(1)) ...
     + 2.8081;

Vt(1) = Voc0;

%% ========================================================================
% ESTADOS
%% ========================================================================

mode = "charge";

rest_counter = 0;

cycle = 0;

%% ========================================================================
% SIMULACIÓN
%% ========================================================================

for k = 2:N

    current_time = t(k-1);

    %% ================================================================
    % MÁQUINA DE ESTADOS
    %% ================================================================

    switch mode

        %% ------------------------------------------------------------
        % CARGA
        %% ------------------------------------------------------------

        case "charge"

            pulse_time = mod(current_time,...
                             pulse_period + rest_period);

            if pulse_time <= pulse_period
                I(k) = I_charge;
            else
                I(k) = 0;
            end

            % FIN CARGA
            if SoC(k-1) >= SOC_max

                mode = "rest_after_charge";

                rest_counter = 0;

            end

        %% ------------------------------------------------------------
        % REPOSO POST CARGA
        %% ------------------------------------------------------------

        case "rest_after_charge"

            I(k) = 0;

            rest_counter = rest_counter + Ts;

            if rest_counter >= rest_time

                mode = "discharge";

            end

        %% ------------------------------------------------------------
        % DESCARGA
        %% ------------------------------------------------------------

        case "discharge"

            pulse_time = mod(current_time,...
                             pulse_period + rest_period);

            if pulse_time <= pulse_period
                I(k) = I_discharge;
            else
                I(k) = 0;
            end

            % FIN DESCARGA
            if Vt(k-1) <= V_min

                mode = "rest_after_discharge";

                rest_counter = 0;

                cycle = cycle + 1;

            end

        %% ------------------------------------------------------------
        % REPOSO POST DESCARGA
        %% ------------------------------------------------------------

        case "rest_after_discharge"

            I(k) = 0;

            rest_counter = rest_counter + Ts;

            if rest_counter >= rest_time

                mode = "charge";

            end

    end

    %% ================================================================
    % MODELO ECM
    %% ================================================================

    [Vt(k), SoC(k), Vrc(k)] = batteryECM_step( ...
                                    SoC(k-1),...
                                    Vrc(k-1),...
                                    I(k),...
                                    Ts,...
                                    Cn,...
                                    eta);

    %% ================================================================
    % LIMITES FÍSICOS
    %% ================================================================

    Vt(k) = min(Vt(k),12.6);

    Vt(k) = max(Vt(k),8.6);

    cycle_number(k) = cycle;

end

%% ========================================================================
% TIEMPO
%% ========================================================================

t = (0:N-1)*Ts;

%% ========================================================================
% RESULTADOS
%% ========================================================================

fprintf('\n=================================================\n');
fprintf('SIMULACIÓN FINALIZADA\n');
fprintf('=================================================\n');

fprintf('Tiempo simulado = %.2f horas\n', sim_hours);

fprintf('Ciclos completados = %d\n', max(cycle_number));

fprintf('SOC final = %.4f\n', SoC(end));

fprintf('Voltaje mínimo = %.4f V\n', min(Vt));

fprintf('Voltaje máximo = %.4f V\n', max(Vt));

%% ========================================================================
% VOLTAJE
%% ========================================================================

figure('Color','w')

plot(t/3600,...
     Vt,...
     'LineWidth',1.5)

xlabel('Tiempo [h]')
ylabel('Voltaje [V]')

title('Gemelo Digital - Voltaje')

grid on

%% ========================================================================
% CORRIENTE
%% ========================================================================

figure('Color','w')

plot(t/3600,...
     I,...
     'LineWidth',1.2)

xlabel('Tiempo [h]')
ylabel('Corriente [A]')

title('Corriente')

grid on

%% ========================================================================
% SOC
%% ========================================================================

figure('Color','w')

plot(t/3600,...
     SoC*100,...
     'LineWidth',1.5)

xlabel('Tiempo [h]')
ylabel('SOC [%]')

title('SOC')

grid on

%% ========================================================================
% CICLOS
%% ========================================================================

figure('Color','w')

plot(t/3600,...
     cycle_number,...
     'LineWidth',1.5)

xlabel('Tiempo [h]')
ylabel('Número ciclo')

title('Conteo de ciclos')

grid on

%% ========================================================================
% TABLA
%% ========================================================================

results = table( ...
            t',...
            Vt,...
            I,...
            SoC,...
            cycle_number,...
            'VariableNames',...
            {'Time_s','Voltage_V','Current_A','SOC','Cycle'});

%% ========================================================================
% EXPORTAR CSV
%% ========================================================================

writetable(results,'DigitalTwin_Data.csv');

fprintf('\n✔ CSV exportado correctamente\n');

%% ========================================================================
% EXPORTAR MAT
%% ========================================================================

save('DigitalTwin_Data.mat',...
     't',...
     'Vt',...
     'I',...
     'SoC',...
     'cycle_number');

fprintf('✔ MAT exportado correctamente\n');