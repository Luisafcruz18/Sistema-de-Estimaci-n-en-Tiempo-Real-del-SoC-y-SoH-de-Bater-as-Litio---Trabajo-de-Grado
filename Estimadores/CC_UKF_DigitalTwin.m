clear; clc; close all;

%% ========================================================================
%                 UKF SOBRE GEMELO DIGITAL
% ========================================================================

%% ========================================================================
% CARGAR DATOS DEL GEMELO DIGITAL
%% ========================================================================

load('DigitalTwin_Data.mat');

%% ========================================================================
% VARIABLES
%% ========================================================================

t_sec = t(:);

V_true = Vt(:);

I_true = I(:);

SOC_real = SoC(:);

%% ========================================================================
% AÑADIR RUIDO SINTÉTICO
%
% Simula sensores reales
%% ========================================================================

rng(1);

sigma_V = 0.01;      % V
sigma_I = 0.001;      % A

V_meas = V_true + sigma_V*randn(length(V_true),1);

I_meas = I_true + sigma_I*randn(length(I_true),1);

%% ========================================================================
% BATERÍA
%% ========================================================================

Cn  = 5.2;
eta = 1;

Qn = Cn*3600;

%% ========================================================================
% MODELO ECM
%% ========================================================================

Rint = 0.20523032;
R0   = 0.55510210;
C0   = 5380.22;

%% ========================================================================
% TIEMPO
%% ========================================================================

Ts = mean(diff(t_sec));

%% ========================================================================
% DINÁMICA RC
%% ========================================================================

alpha_rc = exp(-Ts/(R0*C0));

beta_rc = 0.5*R0*(1-alpha_rc);

%% ========================================================================
% COULOMB COUNTING
%% ========================================================================

SOC_cc = zeros(length(I_meas),1);

SOC_cc(1) = 0.80;

for k = 2:length(I_meas)

    SOC_cc(k) = SOC_cc(k-1) ...
              - (eta*Ts/Qn)*I_meas(k);

    SOC_cc(k) = max(0,min(1,SOC_cc(k)));

end

%% ========================================================================
% UKF
%% ========================================================================

L = 2;

%% ========================================================================
% PARÁMETROS UKF
%% ========================================================================

alpha_ukf = 1e-3;
beta_ukf  = 2;
kappa_ukf = 0;

lambda = alpha_ukf^2*(L+kappa_ukf)-L;

gamma = sqrt(L+lambda);

%% ========================================================================
% PESOS
%% ========================================================================

Wm = zeros(2*L+1,1);
Wc = zeros(2*L+1,1);

Wm(1) = lambda/(L+lambda);

Wc(1) = Wm(1) + (1-alpha_ukf^2+beta_ukf);

for i = 2:2*L+1

    Wm(i) = 1/(2*(L+lambda));

    Wc(i) = Wm(i);

end

%% ========================================================================
% RUIDOS
%% ========================================================================

Q_ukf = diag([1e-10 1e-6]);

R_ukf = 0.08^2;

%% ========================================================================
% INICIALIZACIÓN
%% ========================================================================

N = length(t_sec);

x_est = zeros(L,N);

P = diag([1e-4 1e-4]);

x_est(:,1) = [0.90; 0];

SOC_ukf = zeros(N,1);

SOC_ukf(1) = x_est(1,1);

%% ========================================================================
% LOOP UKF
%% ========================================================================

for k = 2:N

    %% ================================================================
    % SIGMA POINTS
    %% ================================================================

    S = chol(P,'lower');

    Xsigma = zeros(L,2*L+1);

    Xsigma(:,1) = x_est(:,k-1);

    for i = 1:L

        Xsigma(:,i+1)   = x_est(:,k-1) + gamma*S(:,i);

        Xsigma(:,i+1+L) = x_est(:,k-1) - gamma*S(:,i);

    end

    %% ================================================================
    % MODELO PROCESO
    %% ================================================================

    Xsigma_pred = zeros(L,2*L+1);

    for i = 1:2*L+1

        SOC_i = Xsigma(1,i);

        Vrc_i = Xsigma(2,i);

        %% SOC

        SOC_pred = SOC_i ...
                 - (eta*Ts/Qn)*I_meas(k);

        SOC_pred = max(0,min(1,SOC_pred));

        %% RC

        Vrc_pred = alpha_rc*Vrc_i ...
                 + beta_rc*I_meas(k);

        Xsigma_pred(:,i) = [SOC_pred; Vrc_pred];

    end

    %% ================================================================
    % MEDIA PREDICHA
    %% ================================================================

    x_pred = zeros(L,1);

    for i = 1:2*L+1

        x_pred = x_pred + Wm(i)*Xsigma_pred(:,i);

    end

    %% ================================================================
    % COVARIANZA PREDICHA
    %% ================================================================

    P_pred = zeros(L);

    for i = 1:2*L+1

        diff = Xsigma_pred(:,i)-x_pred;

        P_pred = P_pred + Wc(i)*(diff*diff');

    end

    P_pred = P_pred + Q_ukf;

    %% ================================================================
    % MODELO MEDICIÓN
    %% ================================================================

    Ysigma = zeros(1,2*L+1);

    for i = 1:2*L+1

        SOC_i = Xsigma_pred(1,i);

        Vrc_i = Xsigma_pred(2,i);

        %% VOC

        Voc = 16.4800*(SOC_i.^3) ...
            - 35.0493*(SOC_i.^2) ...
            + 27.6924*(SOC_i) ...
            + 2.8081;

        %% Voltaje terminal

        Ysigma(i) = Voc ...
                  - Rint*I_meas(k) ...
                  - Vrc_i;

    end

    %% ================================================================
    % MEDIA MEDICIÓN
    %% ================================================================

    y_pred = 0;

    for i = 1:2*L+1

        y_pred = y_pred + Wm(i)*Ysigma(i);

    end

    %% ================================================================
    % COVARIANZA INNOVACIÓN
    %% ================================================================

    Pyy = 0;

    for i = 1:2*L+1

        diff = Ysigma(i)-y_pred;

        Pyy = Pyy + Wc(i)*diff*diff';

    end

    Pyy = Pyy + R_ukf;

    %% ================================================================
    % COVARIANZA CRUZADA
    %% ================================================================

    Pxy = zeros(L,1);

    for i = 1:2*L+1

        dx = Xsigma_pred(:,i)-x_pred;

        dy = Ysigma(i)-y_pred;

        Pxy = Pxy + Wc(i)*dx*dy;

    end

    %% ================================================================
    % GANANCIA KALMAN
    %% ================================================================

    K = Pxy/Pyy;

    %% ================================================================
    % ACTUALIZACIÓN
    %% ================================================================

    x_est(:,k) = x_pred ...
               + K*(V_meas(k)-y_pred);

    %% Limitar SOC

    x_est(1,k) = min(0.99,max(0.01,x_est(1,k)));

    P = P_pred - K*Pyy*K';

    SOC_ukf(k) = x_est(1,k);

end

%% ========================================================================
% ERRORES
%% ========================================================================

error_cc  = SOC_real - SOC_cc;

error_ukf = SOC_real - SOC_ukf;

RMSE_cc = sqrt(mean(error_cc.^2));

RMSE_ukf = sqrt(mean(error_ukf.^2));

fprintf('\n=================================================\n');
fprintf('RESULTADOS GEMELO DIGITAL\n');
fprintf('=================================================\n');

fprintf('RMSE Coulomb Counting = %.6f\n', RMSE_cc);

fprintf('RMSE UKF              = %.6f\n', RMSE_ukf);

