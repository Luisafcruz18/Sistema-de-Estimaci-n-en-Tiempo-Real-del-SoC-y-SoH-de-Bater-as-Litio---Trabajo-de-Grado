%% ========================================================================
% LOCAL FUNCTION
%% ========================================================================

function err = localTheveninError(x,...
                                  Voc,...
                                  I,...
                                  V_exp,...
                                  Ts)

Rint = abs(x(1));
R0   = abs(x(2));
C0   = abs(x(3));

%% alpha

alpha = exp(-Ts/(R0*C0));

%% initialize

V_sim = zeros(length(V_exp),1);

V_sim(1) = V_exp(1);

%% model

for k = 2:length(V_exp)

    V_sim(k) = Voc(k) ...
             + (V_sim(k-1)-Voc(k-1))*alpha ...
             - (R0-(R0+Rint)*alpha)*I(k-1) ...
             - Rint*I(k);

end

%% mean squared error

err = mean((V_exp - V_sim).^2);

end