clear; clc; close all;

%% ========================================================================
%        THEVENIN 1RC PARAMETER IDENTIFICATION
% ========================================================================

%% ================== LOAD DATA ==================

load('DESCARGA_MANUAL_SAVE_204925_2026-05-08_0915_recorte_23014s_38965s.mat')

data = logSesion;

%% ================== VARIABLES ==================

t_raw = data(:,1);

% datenum -> seconds
t = (t_raw - t_raw(1))*24*3600;

V_exp = data(:,2);
I     = data(:,3);

%% ================== BATTERY PARAMETERS ==================

Cn  = 5.2;
eta = 1;

%% ================== TIME ==================

Ts = mean(diff(t));

fprintf('Ts = %.6f s\n', Ts);

Ts_h = Ts/3600;

%% ================== CURRENT OFFSET ==================

I = I + 0.3;

%% ========================================================================
% EXPERIMENTAL SOC
%% ========================================================================

SoC = zeros(length(I),1);

SoC(1) = 1;

for k = 2:length(I)

    SoC(k) = SoC(k-1) ...
           - (eta*Ts_h/Cn)*I(k-1);

    SoC(k) = max(0,min(1,SoC(k)));

end

%% ========================================================================
% VOC(SoC)
%% ========================================================================

Voc = 16.4800*(SoC.^3) ...
    - 35.0493*(SoC.^2) ...
    + 27.6924*(SoC) ...
    + 2.8081;

%% ========================================================================
% COST FUNCTION
%% ========================================================================

costFunction = @(x) localTheveninError( ...
                    x,...
                    Voc,...
                    I,...
                    V_exp,...
                    Ts);

%% ========================================================================
% INITIAL PARAMETERS
%% ========================================================================

% x = [Rint R0 C0]

x0 = [

0.01     % Rint
0.01     % R0
5000     % C0

];

%% ========================================================================
% OPTIMIZATION
%% ========================================================================

options = optimset( ...
    'Display','iter', ...
    'MaxIter',500, ...
    'TolX',1e-8);

x_opt = fminsearch(costFunction,...
                   x0,...
                   options);

%% ========================================================================
% RESULTS
%% ========================================================================

Rint = x_opt(1);
R0   = x_opt(2);
C0   = x_opt(3);

fprintf('\n=================================================\n');
fprintf('IDENTIFIED PARAMETERS\n');
fprintf('=================================================\n');

fprintf('Rint = %.8f ohm\n', Rint);

fprintf('R0   = %.8f ohm\n', R0);

fprintf('C0   = %.2f F\n', C0);

%% ========================================================================
% SIMULATE FINAL MODEL
%% ========================================================================

alpha = exp(-Ts/(R0*C0));

V_sim = zeros(length(V_exp),1);

V_sim(1) = V_exp(1);

for k = 2:length(V_exp)

    V_sim(k) = Voc(k) ...
             + (V_sim(k-1)-Voc(k-1))*alpha ...
             - (R0-(R0+Rint)*alpha)*I(k-1) ...
             - Rint*I(k);

end

%% ========================================================================
% FINAL ERROR
%% ========================================================================

RMSE = sqrt(mean((V_exp - V_sim).^2));

fprintf('\nRMSE Final= %.6f V (%.2f mV)\n',...
        RMSE,...
        RMSE*1000);

%% ========================================================================
% VOLTAGE PLOT
%% ========================================================================

figure

plot(t/60,V_exp,...
    'LineWidth',2)

hold on

plot(t/60,V_sim,...
    '--',...
    'LineWidth',2)

xlabel('Tiempo [min]')
ylabel('Voltaje [V]')

title('Modelo Thevenin 1RC')

legend('Experimental','Simulado')

grid on

